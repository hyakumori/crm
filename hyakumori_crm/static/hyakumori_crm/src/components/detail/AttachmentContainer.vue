<template>
  <div>
    <content-header
      :content="headerContent"
      :editBtnContent="editBtnContent"
      :loading="loading"
      :permissions="['just_unnecessary']"
    />
    <template v-if="isExpand">
      <attachment-card
        class="mt-4"
        :isUpdate="isUpdate"
        :attaches="archiveExpand"
        :ripple="false"
      />
    </template>
    <template v-else>
      <attachment-card
        class="mt-4"
        :isUpdate="isUpdate"
        :attaches="archiveCollapse"
        :ripple="false"
      />
    </template>
    <p
      v-if="isRequiredExpand && archiveExpand && archiveExpand.length > 0"
      class="expand"
      @click="this.isExpand = !this.isExpand"
    >
      {{ isExpand ? "一部表示する" : "すべて表示する" }}
    </p>
  </div>
</template>

<script>
import ContentHeader from "./ContentHeader";
import ContainerMixin from "./ContainerMixin";
import AttachmentCard from "./AttachmentCard";

export default {
  name: "attachment-container",

  mixins: [ContainerMixin],

  components: {
    ContentHeader,
    AttachmentCard,
  },

  props: {
    isRequiredExpand: {
      type: Boolean,
      default: true,
    },
  },

  data() {
    return {
      id: this.$route.params.id,
      loading: false,
      isExpand: false,
      isUpdate: false,
      archives: [],
    };
  },

  mounted() {
    this.fetchRelatedArchives();
  },

  methods: {
    fetchRelatedArchives() {
      this.loading = true;
      this.$rest(`/forests/${this.id}/related_archives`).then(res => {
        this.archives = res.results;
        this.loading = false;
      });
    },
  },

  computed: {
    archiveExpand() {
      return this.archives;
    },

    archiveCollapse() {
      return this.archives.slice(0, 3);
    },
  },
};
</script>

